package com.jitlogic.zorka.lisp;

public class ReaderException extends LispException {

    private int line, col;

    public ReaderException(int line, int col, String msg) {
        super("("+line+","+col+")"+msg);
        this.line = line;
        this.col = col;
    }

    public ReaderException(int line, int col, String msg, Throwable e) {
        super("("+line+","+col+")"+msg, e);
        this.line = line;
        this.col = col;
    }

    public int getLine() {
        return line;
    }

    public int getCol() {
        return col;
    }
}
